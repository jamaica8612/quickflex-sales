$$;

create or replace function public.quickflex_guard_profile_update()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  if public.quickflex_is_admin() then
    return new;
  end if;

  if auth.uid() = old.id then
    new.id := old.id;
    new.email := old.email;
    new.status := old.status;
    new.role := old.role;
    return new;
  end if;

  raise exception 'profile update is not allowed';
end;
$$;

drop trigger if exists quickflex_guard_profile_update_trigger on public.quickflex_profiles;
create trigger quickflex_guard_profile_update_trigger
before update on public.quickflex_profiles
for each row
execute function public.quickflex_guard_profile_update();

create or replace function public.quickflex_bootstrap_first_profile()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  if not exists (select 1 from public.quickflex_profiles) then
    new.status := 'approved';
    new.role := 'admin';
    if new.display_name is null or new.display_name = '' then
      new.display_name := '관리자';
    end if;
  end if;
  return new;
end;
$$;

drop trigger if exists quickflex_bootstrap_first_profile_trigger on public.quickflex_profiles;
create trigger quickflex_bootstrap_first_profile_trigger
before insert on public.quickflex_profiles
for each row
execute function public.quickflex_bootstrap_first_profile();

create or replace function public.quickflex_ensure_profile(
  profile_email text,
  profile_display_name text,
  profile_driver_type text
)
returns public.quickflex_profiles
language plpgsql
security definer
set search_path = public
as $$
declare
  result public.quickflex_profiles;
begin
  if auth.uid() is null then
    raise exception 'login is required';
  end if;

  insert into public.quickflex_profiles (id, email, display_name, driver_type, status, role, fixed_routes)
  values (
    auth.uid(),
    profile_email,
    coalesce(nullif(profile_display_name, ''), '사용자'),
    case when profile_driver_type = 'fixed' then 'fixed' else 'backup' end,
    'pending',
    'driver',
    '{}'
  )
  on conflict (id) do nothing;

  select *
  into result
  from public.quickflex_profiles
  where id = auth.uid();

  if result.id is null then
    raise exception 'profile could not be created';
  end if;

  return result;
end;
$$;

